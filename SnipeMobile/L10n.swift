import SwiftUI

enum L10n {
    /// First preferred language.
    private static var languageCode: String {
        let preferred = Locale.preferredLanguages.first ?? ""
        let code = String(preferred.prefix(2)).lowercased()
        return code.isEmpty ? "en" : code
    }

    static var isDutch: Bool { languageCode == "nl" }
    static var isFrench: Bool { languageCode == "fr" }
    static var isSpanish: Bool { languageCode == "es" }

    static func string(_ key: String) -> String {
        if isDutch { return dutch[key] ?? english[key] ?? key }
        if isFrench { return french[key] ?? english[key] ?? key }
        if isSpanish { return spanish[key] ?? english[key] ?? key }
        return english[key] ?? key
    }

    static func string(_ key: String, _ arg: String) -> String {
        let template = string(key)
        return String(format: template, arg)
    }

    static func string(_ key: String, _ arg: Int) -> String {
        let template = string(key)
        return String(format: template, arg)
    }

    /// status_meta to localized label.
    static func statusLabel(_ statusMeta: String) -> String {
        let key = "status_\(statusMeta.trimmingCharacters(in: .whitespaces).lowercased())"
        let out = string(key)
        return out == key ? statusMeta : out
    }

